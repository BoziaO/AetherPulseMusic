export function translate(language, key, vars = {}) {
  const lang = messages[language] ? language : "pl";
  let raw = messages[lang][key] ?? messages.pl[key] ?? key;
  if (vars && typeof raw === "string") {
    Object.entries(vars).forEach(([k, v]) => {
      raw = raw.replaceAll(`{${k}}`, String(v));
    });
  }
  return raw;
}
